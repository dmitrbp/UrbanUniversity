# 1. Определение строки
example = "RandomStr"
# 2. Первый символ строки
print(example[0])
# 3. Последний символ строки
print(example[-1])
# 4. Вторая половина строки
print(example[len(example)//2 : ])
# 5. Строка наоборот
print(example[ : : -1])
# 6. Каждый второй символ строки
print(example[0 : : 2])
